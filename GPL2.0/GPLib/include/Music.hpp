#pragma once
#include <string>
#include <iostream>


#include "../SFML/include/SFML/Audio.hpp"
#include "Audio.hpp"

class Music : public Audio
{
private:
	sf::Music _music;
public:

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	/// Cria a música <see cref="Music"/></summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	Music(void);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Deleta a música <see cref="Music"/>. </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	~Music(void);

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>
	/// Carrega um arquivo de música 
	/// </summary>
	/// <param name="Filename">	Caminho do arquivo da música a ser carregado (ogg, wav, flac, aiff, au, raw, paf, svx, nist, voc, ircam, w64,
	/// mat4, mat5 pvf, htk, sds, avr, sd2, caf, wve, mpc2k, rf64) </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void load(std::string Filename) override;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Começa a tocar a música. </summary>
	/// <param name="volume">	(Opcional) Define o volume da música, padrão é 10. </param>
	/// <param name="repeat">	(Opcional) Define se a música deve repetir, padrão é não. </param>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void play(int volume = 10, bool repeat = false) override;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Pausa a música</summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void pause() override;

	////////////////////////////////////////////////////////////////////////////////////////////////////
	/// <summary>	Pausa a música </summary>
	////////////////////////////////////////////////////////////////////////////////////////////////////
	void stop() override;
};

